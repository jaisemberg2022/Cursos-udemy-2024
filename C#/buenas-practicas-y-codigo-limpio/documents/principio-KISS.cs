/*

KISS: es un acrónimo de Keep It Short and Simple y a rasgos 
generales se puede traducir como Mantenlo simple!.

    Este principio establece que la mayoría de sistemas funcionan mejor si se mantienen 
    simples o sencillos a diferencia de aquellos que se hacen complejos de manera innecesaria.
    Es decir, que desde el incio del proyecto la sencillez tiene que ser una meta en el 
    desarrollo y que la complejidad innecesaria debe ser eliminada cada vez que sea detectada. 
    Muchas veces esta complejidad ocurre cuando se diseñan grandes soluciones para objetivos 
    simples.

*/